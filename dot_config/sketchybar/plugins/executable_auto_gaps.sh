#!/bin/bash
# ==============================================================
# AeroSpace 自动窗口间距调整脚本
# ==============================================================
# @author    Hao Feng (F1)
# @file      auto_gaps.sh
# @desc      监听显示器热插拔事件，自动调整 AeroSpace 窗口间距
#
# 功能说明：
#   - 笔记本单屏模式：gap = 1（紧凑布局，节省空间）
#   - 外接/多屏模式：gap = 30（舒适布局，多显示器体验）
#
# 检测逻辑：
#   1. 统计当前连接的显示器数量
#   2. 检查显示器名称是否包含笔记本屏幕关键词
#   3. 只有在 "单屏 + 内屏" 时才使用小间距
#
# 触发方式：
#   在 sketchybarrc 中通过 display_change 事件自动触发
#
# @version   1.0.1 (2026-03-19)
#            - 统一版本格式
#            1.0.0 (2025-02-03)
#            - 初始版本，支持笔记本/外接显示器自动检测
# ==============================================================

# --------------------------------------------------------------
# 配置文件路径
# --------------------------------------------------------------
# AeroSpace 配置文件路径
CONFIG_FILE="$HOME/.config/aerospace/aerospace.toml"

# --------------------------------------------------------------
# 1. 获取显示器信息
# --------------------------------------------------------------
# list-monitors: 列出所有显示器
# --format: 输出格式，%{monitor-name} 表示只输出显示器名称
MONITOR_NAMES=$(aerospace list-monitors --format "%{monitor-name}")

# 统计显示器数量（去除空白行）
# grep -v "^$": 过滤空行
# wc -l: 统计行数
# xargs: 去除前后空白
MONITOR_COUNT=$(echo "$MONITOR_NAMES" | grep -v "^$" | wc -l | xargs)

# --------------------------------------------------------------
# 2. 智能判断逻辑
# --------------------------------------------------------------
# 定义笔记本屏幕的特征关键词（使用正则表达式）
# Built-in: macOS 内置显示器的标准名称
# 内置: 中文系统可能显示的名称
# Retina: Apple Retina 显示器
# Laptop: 通用笔记本关键词
INTERNAL_KEYWORDS="Built-in|内置|Retina|Laptop"

# 默认设置为外接模式（大间距）
MODE_NAME="外接/多屏"
NEW_GAP=30

# 只有在以下条件同时满足时，才判定为笔记本模式：
# - 显示器数量为 1
# - 该显示器名称包含内屏关键词
if [ "$MONITOR_COUNT" -eq 1 ]; then
    if echo "$MONITOR_NAMES" | grep -qE "$INTERNAL_KEYWORDS"; then
        MODE_NAME="笔记本单屏"
        NEW_GAP=1  # 使用小间距
    fi
fi

# --------------------------------------------------------------
# 3. 修改配置文件并重载
# --------------------------------------------------------------
# 使用 sed 修改 aerospace.toml 中的 outer.top 值
# -i '': macOS 原地编辑（需要保留空字符串 ''）
# s/outer.top = [0-9]*/outer.top = $NEW_GAP/: 正则替换
# [0-9]*: 匹配任意数字
sed -i '' "s/outer.top = [0-9]*/outer.top = $NEW_GAP/" "$CONFIG_FILE"

# 重载 AeroSpace 配置使更改生效
aerospace reload-config

# --------------------------------------------------------------
# 4. 发送系统通知（可选，已注释）
# --------------------------------------------------------------
# 如果需要可视化通知，取消下面这行的注释
# osascript -e "display notification \"模式: $MODE_NAME | Gap: $NEW_GAP\" with title \"Aerospace 自动调整\""

# --------------------------------------------------------------
# 5. 调试输出（帮助排查问题）
# --------------------------------------------------------------
# 如果运行有问题，在终端手动执行脚本可以看到下面的输出
echo "--------------------------------"
echo "检测到的显示器数量: $MONITOR_COUNT"
echo "检测到的显示器名称: "
echo "$MONITOR_NAMES"
echo "--------------------------------"
echo "判定结果 -> 模式: $MODE_NAME, Gap: $NEW_GAP"
