#!/bin/bash
# ==============================================================
# 电池状态监控插件 (Geek Glass)
# ==============================================================
# @author    Hao Feng (F1)
# @file      battery.sh
# @desc      显示电池状态、充电指示和百分比进度条
#
# 显示内容：
#   - 充电状态图标：↑ (充电中) / ↓ (放电中)
#   - 电量百分比：数字显示
#   - 进度条：滑块显示电量
#   - 颜色分级（充电中统一深青色，放电中根据电量）：
#     * 充电中：深青色 (0xFF1E7AA6)
#     * ≤ 20%: 红色（电量低）
#     * ≤ 50%: 黄色（中等）
#     * ≤ 80%: 深青色（充足）
#     * > 80%: 绿色（高电量）
#
# 更新频率：10 秒
#
# @version   1.2.0 (2026-03-19)
#            - 统一版本格式
#            1.1.0 (2025-03-05)
#            - 充电状态统一使用深青色
#            - 放电时新增 80% 分级阈值，深青色/绿色四段显示
# ==============================================================

# --------------------------------------------------------------
# 获取电池信息
# --------------------------------------------------------------
# pmset: macOS 电源管理命令
# -g batt: 获取电池信息
# 2>/dev/null: 忽略错误输出
INFO="$(pmset -g batt 2>/dev/null)"

# --------------------------------------------------------------
# 提取电量百分比
# --------------------------------------------------------------
# grep -Eo '[0-9]+%': 正则匹配百分比数字
# head -n1: 取第一行
# tr -d '%': 删除百分号，只保留数字
PERCENT="$(echo "$INFO" | grep -Eo '[0-9]+%' | head -n1 | tr -d '%')"

# --------------------------------------------------------------
# 判断充电状态
# --------------------------------------------------------------
# grep -q "AC Power": 检查是否在使用交流电源（充电中）
if echo "$INFO" | grep -q "AC Power"; then
    STATE="↑"                    # 充电中（向上箭头）
    STATE_COLOR="0xFF5DC8E8"      # 亮青色
    IS_CHARGING=true
else
    STATE="↓"                    # 放电中（向下箭头）
    STATE_COLOR="0xFFFFFFFF"     # 白色
    IS_CHARGING=false
fi

# --------------------------------------------------------------
# 处理电量未知的情况
# --------------------------------------------------------------
if [[ -z "$PERCENT" ]]; then
    PERCENT="--"
fi

# --------------------------------------------------------------
# 根据电量和充电状态选择进度条颜色（ARGB 格式）
# --------------------------------------------------------------
# 0xAARRGGBB: AA=Alpha, RR=Red, GG=Green, BB=Blue
if [[ "$PERCENT" == "--" ]]; then
    # 电量未知：白色
    BAR_COLOR="0xFFFFFFFF"
elif [[ "$IS_CHARGING" == true ]]; then
    # 充电中：统一使用亮青色
    BAR_COLOR="0xFF5DC8E8"
else
    # 放电中：根据电量百分比选择颜色
    if (( PERCENT <= 20 )); then
        # 低电量：红色
        BAR_COLOR="0xFFFF3B30"
    elif (( PERCENT <= 50 )); then
        # 中等电量：黄色
        BAR_COLOR="0xFFFFCC00"
    elif (( PERCENT <= 80 )); then
        # 充足电量：亮青色
        BAR_COLOR="0xFF5DC8E8"
    else
        # 电量：绿色
        BAR_COLOR="0xFF00FF6A"
    fi
fi

# --------------------------------------------------------------
# 更新 SketchyBar 条目
# --------------------------------------------------------------
# battery.state: 充电状态图标
# battery.pct: 电量百分比文本
# battery.slider: 电量进度条
sketchybar \
    --set battery.state label="$STATE" label.color="$STATE_COLOR" \
    --set battery.pct   label="${PERCENT}%" label.color="$BAR_COLOR" \
    --set battery.slider slider.percentage="$PERCENT" slider.highlight_color="$BAR_COLOR"
