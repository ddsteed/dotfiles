#!/bin/bash
# ==============================================================
# 前台应用显示插件 (Geek Glass)
# ==============================================================
# @author    Hao Feng (F1)
# @file      front_app.sh
# @desc      显示当前前台应用的图标和名称
#
# 显示内容：
#   - 应用图标：使用 icon_map.sh 映射到 Nerd Font 图标
#   - 应用名称：完整的应用名称
#
# 触发事件：front_app_switched (前台应用切换时)
#
# @version   1.0.1 (2026-03-19)
#            - 统一版本格式
#            1.0.0 (2025-02-04)
#            - 初始版本，集成图标映射功能
# ==============================================================

# --------------------------------------------------------------
# 加载图标映射函数
# --------------------------------------------------------------
# icon_map.sh 提供 __icon_map() 函数
source "$HOME/.config/sketchybar/plugins/icon_map.sh"

# --------------------------------------------------------------
# 加载主题配置 (如果存在)
# --------------------------------------------------------------
THEME_FILE="$HOME/.config/sketchybar/theme.sh"
if [[ -f "$THEME_FILE" ]]; then
  # shellcheck disable=SC1090
  source "$THEME_FILE"
fi

# --------------------------------------------------------------
# 颜色变量定义 (带默认值回退)
# --------------------------------------------------------------
FG="${FG:-0xFFEDEDED}"          # 前景色（白色）
MUTED="${MUTED:-0xFF8A8F98}"    # 静默色（灰色）
WORKSPACE_FOCUSED="${WORKSPACE_FOCUSED:-0xFF7EDEFF}"  # 聚焦色（天蓝色）

# --------------------------------------------------------------
# 处理前台应用切换事件
# --------------------------------------------------------------
# $SENDER: SketchyBar 传入的事件名称
# $INFO: SketchyBar 传入的应用名称
if [[ "$SENDER" == "front_app_switched" ]]; then
  # 获取前台应用名称
  APP="$INFO"

  # ----------------------------------------------------------
  # 将应用名称映射到图标
  # ----------------------------------------------------------
  __icon_map "$APP"
  # 结果保存在 $icon_result 变量中
  ICON="$icon_result"

  # ----------------------------------------------------------
  # 如果图标映射为空，使用默认图标
  # ----------------------------------------------------------
  # 注意：如果想要隐藏某些应用（如 Finder），在 icon_map.sh
  #       中返回空字符串即可
  if [[ -z "$ICON" ]]; then
    ICON=""  # 默认图标（通用应用图标）
  fi

  # ----------------------------------------------------------
  # 更新 SketchyBar 条目
  # ----------------------------------------------------------
  # $NAME: 当前条目名称 (front_app)
  # icon: 应用图标
  # icon.color: 图标颜色（使用强调色）
  # label: 应用名称
  # label.color: 标签颜色（使用前景色）
  sketchybar --set "$NAME" \
    icon="$ICON" \
    icon.color="$WORKSPACE_FOCUSED" \
    label="$APP" \
    label.color="$FG"
fi
